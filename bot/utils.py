def get_mood(fullness: int, happiness: int) -> str:
    """Определяет настроение питомца"""
    if fullness < 30:
        return "Голодный 🍔"
    elif happiness < 30:
        return "Грустный 😔"
    elif fullness > 80 and happiness > 80:
        return "Счастливый 😊"
    elif fullness > 80:
        return "Сытый 😋"
    elif happiness > 80:
        return "Весёлый 😄"
    else:
        return "Обычное 😐"

def get_progress_bar(value: int) -> str:
    """Возвращает строку прогресса (10 символов)"""
    filled = value // 10
    return "█" * filled + "░" * (10 - filled)